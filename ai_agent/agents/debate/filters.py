CONTEXT_PATTERNS: list[tuple[str, str]] = [
    # "수박" + 정치 키워드 → 진보 내 변절자 낙인
    (r"수박", _POLITICAL_CTX),
    # "그분" + 범죄·의혹 키워드 → 판결 전 단정
    (r"그분", r"(범죄|비리|의혹|몸통|배후|주범)"),
    # "몸통" + 특정인 맥락
    (r"몸통", r"(대통령|의원|후보|정치)"),
    # "탄핵|구속" + 감정 도배 신호
    (r"(탄핵|구속)", r"(당장|빨리|즉각|무조건|꼭|반드시).{0,10}(해라|해|시켜|보내)"),
]

