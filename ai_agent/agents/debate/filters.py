# 내부 유틸
# ════════════════════════════════════════════════════════════════════════════

def _contains_any(text: str, words: list[str]) -> str | None:
    """words 중 text에 포함된 첫 단어 반환, 없으면 None"""
    for w in words:
        if w in text:
            return w
    return None


def _match_suffix(text: str) -> str | None:
    """접사 패턴 매칭. 매칭된 단어가 화이트리스트에 있으면 건너뜀"""
    for pat in SUFFIX_PATTERNS:
        for m in re.finditer(pat, text):
            matched = m.group()
            # 매칭된 단어 자체가 화이트리스트에 있으면 오탐으로 판단
            if not any(matched == w or matched in w or w in matched
                       for w in FALSE_POSITIVE_WHITELIST):
                return matched
    return None


def _match_context(text: str) -> str | None:
    """맥락 의존형 패턴: 트리거 단어 + 주변 정치 키워드 동시 존재"""
    for trigger_pat, ctx_pat in CONTEXT_PATTERNS:
        if re.search(trigger_pat, text) and re.search(ctx_pat, text):
            m = re.search(trigger_pat, text)
            return m.group() if m else "맥락의존표현"
    return None


