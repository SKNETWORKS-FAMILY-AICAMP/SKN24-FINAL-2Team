# 공개 인터페이스
# ════════════════════════════════════════════════════════════════════════════

def quick_filter(text: str) -> Dict:
    """
    사전 기반 1차 필터.
    LLM 호출 전에 실행해 명백한 혐오·비하 표현을 차단.

    Returns
    -------
    {
        "passed"        : bool,
        "violation_type": "none" | "political_slur" | "group_stigma"
                          | "regional_slur" | "threat" | "hate_suffix"
                          | "context_hate",
        "matched"       : str,   # 감지된 표현
        "message"       : str,   # 사용자 안내 문구
    }
    """
    # 0. 일반 욕설·비속어
    matched = _contains_any(text, GENERAL_PROFANITY)
    if matched:
        return {
            "passed": False,
            "violation_type": "profanity",
            "matched": matched,
            "message": (
                f"욕설 또는 비속어('{matched}')가 포함되어 있습니다. "
                "정중한 표현으로 다시 작성해주세요."
            ),
        }

    # 1. 정치인 멸칭
    matched = _contains_any(text, POLITICAL_SLURS)
    if matched:
        return {
            "passed": False,
            "violation_type": "political_slur",
            "matched": matched,
            "message": (
                f"특정 정치인을 비하하는 표현('{matched}')이 포함되어 있습니다. "
                "정치인을 비판할 때는 정책과 행동에 근거한 표현을 사용해주세요."
            ),
        }

    # 2. 집단·진영 비하
    matched = _contains_any(text, GROUP_STIGMA)
    if matched:
        return {
            "passed": False,
            "violation_type": "group_stigma",
            "matched": matched,
            "message": (
                f"특정 정치 집단을 비하하는 표현('{matched}')이 포함되어 있습니다. "
                "지지층 전체를 낙인찍는 표현 대신 구체적인 정책 비판을 해주세요."
            ),
        }

    # 3. 지역 비하
    matched = _contains_any(text, REGIONAL_SLURS)
    if matched:
        return {
            "passed": False,
            "violation_type": "regional_slur",
            "matched": matched,
            "message": (
                f"지역을 비하하는 표현('{matched}')이 포함되어 있습니다. "
                "지역감정을 조장하는 표현은 건전한 토론을 방해합니다."
            ),
        }

    # 4. 위협·배제어
    matched = _contains_any(text, THREAT_WORDS)
    if matched:
        return {
            "passed": False,
            "violation_type": "threat",
            "matched": matched,
            "message": (
                f"위협적이거나 폭력적인 표현('{matched}')이 포함되어 있습니다. "
                "토론에서는 상대의 주장을 논리로 반박해주세요."
            ),
        }

    # 5. 혐오 접사 패턴
    matched = _match_suffix(text)
    if matched:
        return {
            "passed": False,
            "violation_type": "hate_suffix",
            "matched": matched,
            "message": (
                f"혐오·비하 접사가 포함된 표현('{matched}')이 감지되었습니다. "
                "상대를 벌레나 특정 집단에 비유하는 표현은 삼가주세요."
            ),
        }

    # 6. 맥락 의존형 패턴
    matched = _match_context(text)
    if matched:
        return {
            "passed": False,
            "violation_type": "context_hate",
            "matched": matched,
            "message": (
                f"정치적 맥락에서 비하로 해석될 수 있는 표현('{matched}')이 감지되었습니다. "
                "해당 표현이 의도한 바를 다른 방식으로 표현해주세요."
            ),
        }

    return {"passed": True, "violation_type": "none", "matched": "", "message": ""}
